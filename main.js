cc.game.onStart = function(){
    if(!cc.sys.isNative && document.getElementById("cocosLoading")) //If referenced loading.js, please remove it
        document.body.removeChild(document.getElementById("cocosLoading"));

    // Pass true to enable retina display, disabled by default to improve performance
    cc.view.enableRetina(true);
    // Adjust viewport meta
    cc.view.adjustViewPort(true);
    // Setup the resolution policy and design resolution size
    cc.view.setDesignResolutionSize(1900, 900, cc.ResolutionPolicy.FIXED_HEIGHT);
    // The game will be resized when browser size change
    cc.view.resizeWithBrowserSize(true);
    //load resources
    cc.LoaderScene.preload(g_resources, function () {
        GC.winSize = cc.director.getWinSize();
        PLAYER = new Player({x: 1200, y: 500, hero: new Handsome()});
        var map = new Map({mapConfig: "sunsetCity", callback: function(){
            this.addChild(new TalkLayout({
                npc: "Ninja",
                words: ["欢迎来到这个世界~ 我是你的新手教练，你可以叫我Ninja", "首先来介绍下操作，方向键移动，X键跳跃，Z键攻击\n就这么多啦~ 去活动活动吧~  小心别挂了~", ]
            }))
        }});
        map.addPlayer(PLAYER);
        cc.director.runScene(map);
    }, this);
};
cc.game.run();